@app.post("/api/tools/<tool_name>")
def practical_tool(tool_name):
    demos = {
        "list": list_tools,
        "openssl": demo_openssl,
        "gnupg": demo_gnupg,
        "password-attack": demo_password_attack,
        "netcat": demo_netcat_guide,
        "wireshark": demo_wireshark_guide,
    }

    if tool_name not in demos:
        return jsonify({"ok": False, "error": "Unknown practical tool: " + tool_name}), 404

    result = capture_output(demos[tool_name])
    return jsonify({"ok": result["ok"], "tool": tool_name, "output": result["output"], "artifacts": artifact_list()})


@app.get("/files/<path:relative_path>")
def files(relative_path):
    target = (ROOT / relative_path).resolve()
    if not str(target).startswith(str(ROOT)):
        return jsonify({"ok": False, "error": "Invalid file path"}), 400
    return send_from_directory(target.parent, target.name)


