@app.post("/api/hide")
def hide_secret():
    payload = request.get_json(silent=True) or {}
    secret = payload.get("secret", "").strip() or "Professor demo secret message"
    steps = []

    if not INPUT_IMAGE.exists():
        return jsonify({"ok": False, "error": "data/input.png is missing", "steps": steps}), 400

    h = Handshake()
    with contextlib.redirect_stdout(io.StringIO()):
        h.client_hello()
        h.server_hello()
        cert = h.send_certificate()
        cert_ok = h.verify_certificate(cert)
        session_key = h.generate_session_key()

    steps.append({"label": "Handshake complete", "status": "success", "detail": "Client/server hello completed."})
    steps.append(
        {
            "label": "Certificate valid" if cert_ok else "Certificate invalid",
            "status": "success" if cert_ok else "danger",
            "detail": "Certificate hash matched cert/my_cert.json." if cert_ok else "Certificate hash mismatch.",
        }
    )

    if not cert_ok:
        return jsonify({"ok": False, "error": "Handshake certificate verification failed", "steps": steps}), 400

    gk = GANKey()
    attacker = Attacker()
    cipher_attack = CipherAttack()
    gan_noise = gk.generate_noise()
    final_key = session_key + gan_noise
    attacker_score = attacker.try_key(final_key)

    if attacker_score < 0.3:
        gan_noise = gk.generate_noise()
        final_key = session_key + gan_noise
        attacker_score = attacker.try_key(final_key)

    aes = AESCrypto(final_key)
    ciphertext = aes.encrypt(secret)
    ImageSteg().hide(str(INPUT_IMAGE), str(OUTPUT_IMAGE), ciphertext)
    cipher_score = cipher_attack.check_cipher(ciphertext)

    otp = OTP()
    code = otp.generate()
    otp_ok = otp.verify(code)

    pond = PondAuth(aes.key, 5)
    with contextlib.redirect_stdout(io.StringIO()):
        pond.generate()
    pond_ok = pond.verify(pond.correct)
    cert_file_ok = verify_certificate(str(CERT_PATH))

    steps.extend(
        [
            {"label": "Session key generated", "status": "success", "detail": rel(SESSION_FILE)},
            {
                "label": "GAN noise generated",
                "status": "success",
                "detail": rel(GAN_NOISE_FILE),
            },
            {
                "label": "Final AES key derived",
                "status": "info",
                "detail": "SHA-256 key preview: " + sha256_preview(final_key),
            },
            {
                "label": "Ciphertext created",
                "status": "success",
                "detail": ciphertext[:96] + ("..." if len(ciphertext) > 96 else ""),
            },
            {"label": "Ciphertext hidden in image", "status": "success", "detail": rel(OUTPUT_IMAGE)},
            {
                "label": "OTP, pond, and certificate checks passed",
                "status": "success" if otp_ok and pond_ok and cert_file_ok else "danger",
                "detail": "OTP verified, pond " + str(pond.correct) + " selected, certificate rechecked.",
            },
        ]
    )

    return jsonify(
        {
            "ok": otp_ok and pond_ok and cert_file_ok,
            "secretLength": len(secret),
            "attackerScore": round(float(attacker_score), 4),
            "cipherAttackScore": round(float(cipher_score), 4),
            "ciphertextPreview": ciphertext[:140] + ("..." if len(ciphertext) > 140 else ""),
            "outputImage": "/files/" + rel(OUTPUT_IMAGE),
            "steps": steps,
            "artifacts": artifact_list(),
        }
    )


@app.post("/api/extract")
def extract_secret():
    missing = [rel(path) for path in (SESSION_FILE, GAN_NOISE_FILE, OUTPUT_IMAGE) if not path.exists()]
    if missing:
        return jsonify({"ok": False, "error": "Missing required artifacts: " + ", ".join(missing)}), 400

    h = Handshake()
    session_key = h.load_session_key()
    gan_noise = GANKey().load_noise()
    final_key = session_key + gan_noise
    aes = AESCrypto(final_key)
    ciphertext = ImageSteg().extract(str(OUTPUT_IMAGE))
    secret = aes.decrypt(ciphertext)

    steps = [
        {"label": "Session key loaded", "status": "success", "detail": rel(SESSION_FILE)},
        {"label": "GAN noise loaded", "status": "success", "detail": rel(GAN_NOISE_FILE)},
        {"label": "Final AES key rebuilt", "status": "info", "detail": "SHA-256 key preview: " + sha256_preview(final_key)},
        {
            "label": "Ciphertext extracted",
            "status": "success",
            "detail": ciphertext[:96] + ("..." if len(ciphertext) > 96 else ""),
        },
        {"label": "Secret decrypted", "status": "success", "detail": secret},
    ]

    return jsonify(
        {
            "ok": True,
            "secret": secret,
            "ciphertextPreview": ciphertext[:140] + ("..." if len(ciphertext) > 140 else ""),
            "steps": steps,
        }
    )


