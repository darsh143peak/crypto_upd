def rel(path):
    return str(path.relative_to(ROOT)).replace("\\", "/")


def file_info(path, purpose):
    path = Path(path)
    info = {
        "path": rel(path),
        "purpose": purpose,
        "exists": path.exists(),
        "size": path.stat().st_size if path.exists() else 0,
        "preview": None,
        "kind": "file",
    }

    if path.suffix.lower() in {".png", ".jpg", ".jpeg"}:
        info["kind"] = "image"
        info["url"] = "/files/" + rel(path)
    elif path.exists() and path.stat().st_size <= 4096 and path.suffix.lower() not in {".bin", ".gpg", ".enc", ".pem"}:
        try:
            info["preview"] = path.read_text(encoding="utf-8", errors="replace")[:800]
        except OSError:
            info["preview"] = None

    return info


def sha256_preview(data):
    return hashlib.sha256(data).hexdigest()[:24]


def capture_output(fn):
    stream = io.StringIO()
    try:
        with contextlib.redirect_stdout(stream), contextlib.redirect_stderr(stream):
            fn()
        return {"ok": True, "output": stream.getvalue()}
    except Exception as exc:
        return {"ok": False, "output": stream.getvalue() + "\n" + str(exc)}


def artifact_list():
    artifacts = [
        file_info(INPUT_IMAGE, "Original carrier image used by image steganography."),
        file_info(OUTPUT_IMAGE, "Stego image containing the AES ciphertext."),
        file_info(SESSION_FILE, "Session key generated by the SSL-style handshake."),
        file_info(GAN_NOISE_FILE, "GAN-generated or random noise mixed into the final AES key."),
        file_info(CERT_PATH, "JSON certificate verified during handshake and access checks."),
    ]

    practical_purposes = {
        "openssl": "OpenSSL generated keys, certificate, encrypted file, and decrypted message.",
        "gnupg": "GnuPG message, signature, encrypted output, decrypted output, and local keyring.",
        "password_attack": "Weak hash, wordlist, and cracked-password evidence.",
        "netcat": "Plaintext, AES ciphertext, and local decrypt check for network transfer.",
    }

    if PRACTICAL_BASE_DIR.exists():
        for folder, purpose in practical_purposes.items():
            folder_path = PRACTICAL_BASE_DIR / folder
            if folder_path.exists():
                for item in sorted(folder_path.iterdir()):
                    if item.is_file():
                        artifacts.append(file_info(item, purpose))

    return artifacts


